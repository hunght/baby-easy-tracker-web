#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';

const cwd = process.cwd();
const outputFile = path.join(cwd, 'lib', 'convex-api.generated.ts');
const includePrefixArg = process.argv.find((arg) => arg.startsWith('--include-prefix='));
const includePrefixes = includePrefixArg
  ? includePrefixArg
      .slice('--include-prefix='.length)
      .split(',')
      .map((prefix) => prefix.trim())
      .filter(Boolean)
  : [];

function isObject(value) {
  return typeof value === 'object' && value !== null;
}

function pickString(...values) {
  for (const value of values) {
    if (typeof value === 'string' && value.trim().length > 0) {
      return value.trim();
    }
  }
  return null;
}

function normalizeFunctionType(value) {
  if (!value) return null;
  const lowered = value.toLowerCase();
  if (lowered.includes('query')) return 'query';
  if (lowered.includes('mutation')) return 'mutation';
  if (lowered.includes('action')) return 'action';
  return null;
}

function normalizeVisibility(value) {
  if (!value) return 'unknown';
  const lowered = value.toLowerCase();
  if (lowered.includes('public')) return 'public';
  if (lowered.includes('internal')) return 'internal';
  return 'unknown';
}

function normalizeModulePath(modulePath) {
  return modulePath.replace(/\.(js|ts|mjs|cjs)$/i, '');
}

function normalizeIdentifierPath(identifier) {
  return identifier
    .replace(/\.(js|ts|mjs|cjs):/i, ':')
    .replace(/\.(js|ts|mjs|cjs)$/i, '');
}

function listSpecFiles() {
  return fs
    .readdirSync(cwd)
    .filter((name) => /^function_spec_\d+\.json$/.test(name))
    .map((name) => {
      const fullPath = path.join(cwd, name);
      const stat = fs.statSync(fullPath);
      return { name, fullPath, mtimeMs: stat.mtimeMs };
    })
    .sort((a, b) => b.mtimeMs - a.mtimeMs);
}

function toFunctionEntries(specJson) {
  const functions = Array.isArray(specJson?.functions)
    ? specJson.functions
    : Array.isArray(specJson)
      ? specJson
      : [];

  const entries = [];
  for (const fn of functions) {
    if (!isObject(fn)) continue;
    const identifier = pickString(
      fn.identifier,
      fn.udfPath,
      fn.path,
      fn.functionName,
      fn.name,
    );
    const type = normalizeFunctionType(
      pickString(fn.functionType, fn.udfType, fn.type, fn.kind),
    );
    if (!identifier || !type) continue;

    const [modulePathRaw, exportNameRaw] = identifier.split(':');
    const modulePath = normalizeModulePath(modulePathRaw?.trim() || '');
    if (!modulePath) continue;

    const exportName = exportNameRaw?.trim() || 'default';
    const visibility = normalizeVisibility(
      pickString(
        fn.visibility,
        isObject(fn.visibility) ? fn.visibility.kind : null,
        fn.udfVisibility,
        fn.access,
      ),
    );
    const runtimePath =
      exportName === 'default'
        ? modulePath
        : `${modulePath}:${exportName}`;
    entries.push({
      identifier,
      modulePath,
      exportName,
      type,
      visibility,
      runtimePath: normalizeIdentifierPath(runtimePath),
    });
  }

  entries.sort((a, b) => a.identifier.localeCompare(b.identifier));
  return entries;
}

function createNode() {
  return {
    children: new Map(),
    functions: new Map(),
  };
}

function insertEntry(root, entry) {
  let node = root;
  const segments = entry.modulePath.split('/').filter(Boolean);
  for (const segment of segments) {
    if (!node.children.has(segment)) {
      node.children.set(segment, createNode());
    }
    node = node.children.get(segment);
  }
  node.functions.set(entry.exportName, {
    path: entry.runtimePath,
    type: entry.type,
    visibility: entry.visibility,
  });
}

function renderKey(key) {
  return /^[$A-Z_][0-9A-Z_$]*$/i.test(key) ? key : JSON.stringify(key);
}

function renderNode(node, indent) {
  const lines = [];

  const childEntries = [...node.children.entries()].sort((a, b) =>
    a[0].localeCompare(b[0]),
  );
  for (const [segment, child] of childEntries) {
    lines.push(`${indent}${renderKey(segment)}: {`);
    lines.push(renderNode(child, `${indent}  `));
    lines.push(`${indent}},`);
  }

  const functionEntries = [...node.functions.entries()].sort((a, b) =>
    a[0].localeCompare(b[0]),
  );
  for (const [exportName, info] of functionEntries) {
    lines.push(
      `${indent}${renderKey(exportName)}: { path: ${JSON.stringify(info.path)}, type: ${JSON.stringify(info.type)}, visibility: ${JSON.stringify(info.visibility)} },`,
    );
  }

  return lines.join('\n');
}

function generateSource(entries, sourceFileName) {
  const root = createNode();
  for (const entry of entries) {
    insertEntry(root, entry);
  }

  const header = [
    '// Generated by scripts/generate-convex-client.mjs',
    `// Source: ${sourceFileName}`,
    includePrefixes.length > 0
      ? `// Filter: include prefixes ${includePrefixes.map((prefix) => JSON.stringify(prefix)).join(', ')}`
      : '// Filter: none (all functions)',
    '// Do not edit this file manually.',
    '',
    "export type GeneratedConvexFunctionType = 'query' | 'mutation' | 'action';",
    "export type GeneratedConvexVisibility = 'public' | 'internal' | 'unknown';",
    '',
    'export const convexApi = {',
  ];
  const body = renderNode(root, '  ');
  const footer = ['} as const;', ''];

  return [...header, body, ...footer].join('\n');
}

function main() {
  const specFiles = listSpecFiles();
  if (specFiles.length === 0) {
    console.error('No function spec file found. Run `npx convex function-spec --file` first.');
    process.exit(1);
  }

  const latest = specFiles[0];
  const parsed = JSON.parse(fs.readFileSync(latest.fullPath, 'utf8'));
  const entries = toFunctionEntries(parsed);
  const filteredEntries =
    includePrefixes.length === 0
      ? entries
      : entries.filter((entry) =>
          includePrefixes.some(
            (prefix) =>
              entry.runtimePath.startsWith(prefix) || entry.modulePath.startsWith(prefix),
          ),
        );

  if (filteredEntries.length === 0) {
    console.error(`No functions found in ${latest.name}.`);
    process.exit(1);
  }

  const source = generateSource(filteredEntries, latest.name);
  fs.writeFileSync(outputFile, source, 'utf8');
  console.log(
    `Wrote ${path.relative(cwd, outputFile)} from ${latest.name} (${filteredEntries.length} functions).`,
  );
}

main();
